import { z } from 'zod';
import type { UIMessage } from 'ai';
import type { Suggestion } from './db/schema';

// Legacy artifact types for backward compatibility with existing database records
export type ArtifactKind = 'text' | 'image' | 'sheet' | 'story';

export type DataPart = { type: 'append-message'; message: string };

export const messageMetadataSchema = z.object({
  createdAt: z.string(),
});

export type MessageMetadata = z.infer<typeof messageMetadataSchema>;

// Legacy tool types removed - no tool orchestration in simplified chapter writing interface

export type CustomUIDataTypes = {
  textDelta: string;
  imageDelta: string;
  sheetDelta: string;
  codeDelta: string;
  suggestion: Suggestion;
  appendMessage: string;
  id: string;
  title: string;
  kind: ArtifactKind;
  clear: null;
  finish: null;
};

export type ChatMessage = UIMessage<
  MessageMetadata,
  CustomUIDataTypes,
  ChatTools
>;

export interface Attachment {
  name: string;
  url: string;
  contentType: string;
}
